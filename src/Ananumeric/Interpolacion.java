    public double ResolverNewton(double a[], double value, int n)
    {
        double rpta;
        double j = dy[0];
        double w = 1;
        for(int i =1; i<n; i++)
        {
            j = j + a[i]*(value - dx[i-1])*w;
            w = w*(value - dx[i-1]);
        }       
        return j;
    }
    
