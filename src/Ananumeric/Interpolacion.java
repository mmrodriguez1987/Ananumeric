    public void InterpolacionLagrange()
    {
        int i, j, n;
        double c[], tc[],coefficients[], d, t;

        n = Integer.parseInt(jTextField1.getText() + 1);
        coefficients = new double[n];
        for (i = 0; i < n; i++) {
            coefficients[i] = 0.0;
        }
        // c[] are the coefficients of P(x) = (x-x[0])(x-x[1])...(x-x[n-1])
        c = new double[n+1];
        c[0] = 1.0;
        for (i = 0; i < n; i++) {
            for (j = i; j > 0; j--) {
                c[j] = c[j-1] - c[j] * dx[i];
            }
            c[0] *= (-dx[i]);
            c[i+1] = 1;
        }

        tc = new double[n];
        for (i = 0; i < n; i++) {
            // d = (x[i]-x[0])...(x[i]-x[i-1])(x[i]-x[i+1])...(x[i]-x[n-1])
            d = 1;
            for (j = 0; j < n; j++) {
                if (i != j) {
                    d *= (dx[i] - dx[j]);
                }
            }
          /*  if (d == 0.0) {
                // This happens only when two abscissas are identical.
                throw new ArithmeticException
                    ("Identical abscissas cause division by zero.");
            }*/
            t = dy[i] / d;
            // Lagrange polynomial is the sum of n terms, each of which is a
            // polynomial of degree n-1. tc[] are the coefficients of the i-th
            // numerator Pi(x) = (x-x[0])...(x-x[i-1])(x-x[i+1])...(x-x[n-1]).
            tc[n-1] = c[n];     // actually c[n] = 1
            coefficients[n-1] += t * tc[n-1];
            for (j = n-2; j >= 0; j--) {
                tc[j] = c[j+1] + tc[j+1] * dx[i];
                coefficients[j] += t * tc[j];
            }            
        }        
        for(int k=0; k<n; k++)
        {
        System.out.println(c[k]);
        }
        
    }    
    
    public void DiferenciasDivididas()
    {
          int i, j, n;
        double divdiff[], a[], denominator;     

        n = Integer.parseInt(jTextField1.getText());
        divdiff = new double[n];
        for (i = 0; i < n; i++) {
            divdiff[i] = dy[i];      // initialization           
        }
        
        for (i = 1; i < n; i++) {
            for (j = 0; j < n-i; j++) {
                denominator = dx[j+i] - dx[j];
                if (denominator == 0.0) {
                    // This happens only when two abscissas are identical.
                    JOptionPane.showMessageDialog(null,"Las abcisas son iguales","Administrador",JOptionPane.INFORMATION_MESSAGE);
                }
                divdiff[j] = (divdiff[j+1] - divdiff[j]) / denominator;
            }        
       jTextArea1.setText(jTextArea1.getText()  + "\n" + "Diferencias Divididas: ");
       jTextArea1.setText(jTextArea1.getText() + "\n" + "Orden " +i+": ");
        for(int s =0; s<n-i; s++)    
            jTextArea1.setText(jTextArea1.getText() +"\n"+ divdiff[s]);
        }
    }    
    
